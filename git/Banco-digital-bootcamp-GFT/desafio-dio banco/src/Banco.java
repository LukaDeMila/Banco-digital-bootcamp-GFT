import java.util.List;
public class Banco {
// manter todos os atributos como privados, pois se quisermos utilizar os métodos gets e sets poderemos//
	private String nome;
	private List<Conta> contas;

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}
	public List<Conta> getContas() {
		return contas;
	}

	public void setContas(List<Conta> contas) {
		this.contas = contas;
	}
}
